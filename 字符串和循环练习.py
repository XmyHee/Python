#当然，请看下面这个简单的小程序示例，它可以用来练习字符串处理和循环：

def count_vowels(word):
    vowels = 'aeiouAEIOU'
    count = 0

    for char in word:
        if char in vowels:
            count += 1

    return count

def main():
    word = input("请输入一个单词或短语：")
    vowel_count = count_vowels(word)
    print("您输入的单词或短语中有", vowel_count, "个元音字母。")

main()

