
#当你刚开始学习Python时，以下是一些入门级的代码示例，涵盖了一些基本的概念和常用的操作：

#打印语句：
print("Hello, World!") # Hello, World!
#变量和数据类型：
# 字符串
name = "Alice"
print("My name is", name) #My name is Alice

# 整数
age = 25
print("I am", age, "years old") #I am 25 years old

# 浮点数
height = 1.75
print("I am", height, "meters tall") #I am 1.75 meters tall

# 布尔值
is_student = True
print("I am a student:", is_student) #I am a student: True
#算术操作：
# 加法
a = 5
b = 3
sum = a + b
print("Sum:", sum) #Sum: 8

# 减法
difference = a - b
print("Difference:", difference) #Difference: 2

# 乘法
product = a * b
print("Product:", product) #Product: 15

# 除法
quotient = a / b
print("Quotient:", quotient) #Quotient: 1.6666666666666667

# 取余
remainder = a % b
print("Remainder:", remainder) #Remainder: 2
#条件语句：
age = 18
if age >= 18:
    print("You are an adult")
else:
    print("You are not an adult") #You are an adult
#循环：
# for循环
for i in range(1, 6):
    print(i) #1 2 3 4 5

# while循环
count = 0
while count < 5:
    print(count)
    count += 1
# 0 1 2  3 4
#这些示例可以帮助你开始编写一些简单的Python代码。记得尝试运行它们并理解每个示例的输出结果。